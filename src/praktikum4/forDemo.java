package praktikum4;

public class forDemo {

	public static void main(String[] args) {
		for (int i = 1; i<11; i++){
			System.out.println("for 1: "+i);
		}
		for (int i = 10; i>=-3; i--){
			System.out.println("for 1: "+i);
		}
		for (int i = 10, j=2; i>=-3 & j<10;i--, i++){
			System.out.println("for 3: "+i+" "+j);
		}
		
	}

}
