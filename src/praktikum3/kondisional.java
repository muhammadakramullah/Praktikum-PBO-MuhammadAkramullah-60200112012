package praktikum3;

public class kondisional {

	public static void main(String[] args) {
		int nilai1 = 1, nilai2= 2;
		
		if ((nilai1 == 1) && (nilai2 == 2)){
			System.out.println("nilai1 =1 AND  nilai2=2");
		}
		if ((nilai1 == 1) || (nilai2 == 1)){
			System.out.println("nilai1 =1 OR  nilai2=1");
		}
	}

}
